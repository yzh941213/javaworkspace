package com.lang._double;

public class _Double {

    public static void main(String[] args) {

        /**
         * double 的比较是将double转换为一个long来比较
         */

        System.out.println(Double.parseDouble("13"));

        System.out.println(Double.isFinite(12.3));

        System.out.println(Double.compare(12.23,12.23));

    }
}
