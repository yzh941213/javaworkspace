package com.web.entity;

public class Friend {
	private Integer friendAutoid;
	private String name;
	private String url;
	public Integer getFriendAutoid() {
		return friendAutoid;
	}
	public void setFriendAutoid(Integer friendAutoid) {
		this.friendAutoid = friendAutoid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
}
